#!/usr/bin/sh

# Vivacious Colors GTK Theme
# http://www.ravefinity.com/p/vivacious-colors-gtk-theme.html
sudo add-apt-repository ppa:ravefinity-project/ppa
sudo apt-get update
sudo apt-get install vivacious-colors-gtk-dark vivacious-colors-gtk-light

# Vivacious Colors Icon Theme 
# http://www.ravefinity.com/p/vivacious-colors-gtk-icon-theme.html
sudo apt-get install vivacious-colors vivacious-folder-colors-addon

# Use Unity tweak tool to configure
sudo apt-get install unity-tweak-tool

